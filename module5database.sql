What do you understand By Database
A database is an organized collection of structured information, or data, typically stored 
electronically in a computer system. A database is usually controlled by a database management system (DBMS).

What is Normalization?
Normalization is the process to eliminate data redundancy and enhance data integrity in the table. Normalization 
also helps to organize the data in the database. It is a multi-step process that sets the data into tabular form 
and removes the duplicated data from the relational tables.

What is Difference between DBMS and RDBMS? 
DBMS stands for Database Management System, and RDBMS is the acronym for the Relational Database Management system. In 
DBMS, the data is stored as a file, whereas in RDBMS, data is stored in the form of tables.

What is MF Cod Rule of RDBMS Systems?
Codd's rule in DBMS also known as Codd's 12 rules/commandments is a set of thirteen rules (numbered 0 to 12) that define 
a database to be a correct Relational Database Management System (RDBMS). If a database follows Codd's 12 rules, it is called 
a True relational database management system.

What do you understand By Data Redundancy?
Data redundancy occurs when the same piece of data exists in multiple places, whereas data inconsistency is when the same 
data exists in different formats in multiple tables. Unfortunately, data redundancy can cause data inconsistency, which can 
provide a company with unreliable and/or meaningless information.

What is DDL Interpreter?
DDL Interpreter DDL expands to Data Definition Language. DDL Interpreter as the name suggests interprets the DDL statements 
such as schema definition statements like create, delete, etc. The result of this interpretation is a set of a table that 
contains the meta-data which is stored in the data dictionary.

What is DML Compiler in SQL?
A data manipulation language (DML) is a computer programming language used for adding (inserting), deleting, and modifying 
(updating) data in a database. A DML is often a sublanguage of a broader database language such as SQL, with the DML comprising 
some of the operators in the language.

What is SQL Key Constraints writing an Example of SQL Key Constraints
SQL constraints are used to specify rules for the data in a table. Constraints are used to limit the type of data that can go 
into a table. This ensures the accuracy and reliability of the data in the table. If there is any violation between the constraint 
and the data action, the action is aborted.

What is save Point? How to create a save Point write a Query?
A SAVEPOINT is a point in a transaction when you can roll the transaction back to a certain point without rolling back the entire 
transaction. The syntax for a SAVEPOINT command is as shown below. SAVEPOINT SAVEPOINT_NAME; This command serves only in the creation 
of a SAVEPOINT among all the transactional statements.

What is trigger and how to create a Trigger in SQL?
A trigger is a special type of stored procedure that automatically runs when an event occurs in the database server. DML triggers 
run when a user tries to modify data through a data manipulation language (DML) event. DML events are INSERT, UPDATE, or DELETE statements 
on a table or view.
